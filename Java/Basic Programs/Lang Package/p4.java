package com.dxc.demo;
import java.lang.Math;

public class p4 {
	 public static void main(String[] args) {
		    double num1 = 25.5;
		    double num2 = -30.5;
		    System.out.println(Math.abs(num2)); //convert -ve value to +ve
		    System.out.println(Math.max(num1, num2)); //gives maximum
		    System.out.println(Math.sqrt(num1)); //give squareroot
	 }

}
