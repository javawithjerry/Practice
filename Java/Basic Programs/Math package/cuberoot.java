package com.mathpackage;

import java.lang.Math;

public class cuberoot {

   public static void main(String[] args) {

      
      double x = 125;
      double y = 100;

     
      System.out.println("Cube root for x = " + Math.cbrt(x));
      System.out.println("Cube root for y = " + Math.cbrt(y));
      System.out.println("Cube root for value (-36)= " + Math.cbrt(-36));
   }
}