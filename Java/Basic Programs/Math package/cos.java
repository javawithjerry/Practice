package com.mathpackage;

import java.lang.Math;

public class cos{

   public static void main(String[] args) {

      double x = 45.0;
      double y = 17.0;
   
      System.out.println("Cosine value for x = " + Math.cos(x));
      System.out.println("Cosine value for y = " + Math.cos(y));
   }
}