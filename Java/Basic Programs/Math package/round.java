package com.mathpackage;

import java.lang.Math;

public class round {

   public static void main(String[] args) {

      float x = 165.9874f;
      float y = 976.134f;

      System.out.println("Closest int value for the float x(round) =" + Math.round(x));
      System.out.println("Closest int value for the float y(round) =" + Math.round(y));
   }
}
