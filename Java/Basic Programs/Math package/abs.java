package com.mathpackage;

import java.lang.Math;

public class abs {

   public static void main(String[] args) {

      
      double x = -4876.1874d;
      double y = -0.0d;
   
    
      System.out.println("Absolute value of x  = " + Math.abs(x));
      System.out.println("Absolute value of y  = " + Math.abs(y));
      System.out.println(" ");
      System.out.println("Absolute value for (-9999.555d) = " + Math.abs(-9999.555d));
   }
}
